import 'package:flutter/material.dart';
import 'package:youtube_player_flutter/youtube_player_flutter.dart';

SingleChildScrollView buildAulas(
    YoutubePlayerController _controller, bool isPortugues) {
  return SingleChildScrollView(
      child: Column(
    children: [
      Text(
        'Escolha sua aula',
        style: TextStyle(fontSize: 40),
      ),
      YoutubePlayer(
        controller: _controller,
        showVideoProgressIndicator: true,
        progressIndicatorColor: Colors.amber,
        progressColors: ProgressBarColors(
            playedColor: Colors.amber, handleColor: Colors.amberAccent),
      ),
      SizedBox(
        height: 20,
      ),
      ...buildBotoes(_controller, isPortugues),
    ],
  ));
}

List<Widget> buildBotoes(
    YoutubePlayerController _controller, bool isPortugues) {
  List<Widget> todosBotos = [];

  int aula = 1;
  if (isPortugues)
    todosIDsPlaylistAlfabeto().forEach((element) {
      todosBotos.add(botao(element, aula, _controller));
      aula++;
    });
  else
    todosIDsMatematica().forEach((element) {
      todosBotos.add(botao(element, aula, _controller));
      aula++;
    });

  return todosBotos;
}

Widget botao(String url, int numero, YoutubePlayerController _controller) {
  return Padding(
    padding: EdgeInsets.only(left: 30, right: 30),
    child: SizedBox(
      width: double.infinity,
      child: ElevatedButton(
          onPressed: () => _controller.load(url),
          child: Text('Aula n√∫mero $numero')),
    ),
  );
}

